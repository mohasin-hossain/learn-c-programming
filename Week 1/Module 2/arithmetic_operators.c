#include <stdio.h>

int main() {
    int a = 10, b = 3, result;

    result = a + b; // Addition
    printf("a + b = %d\\n", result);

    result = a - b; // Subtraction
    printf("a - b = %d\\n", result);

    result = a * b; // Multiplication
    printf("a * b = %d\\n", result);

    result = a / b; // Division
    printf("a / b = %d\\n", result);

    result = a % b; // Modulus
    printf("a %% b = %d\\n", result);

    a++; // Increment
    printf("a++ = %d\\n", a);

    a--; // Decrement
    printf("a-- = %d\\n", a);

    return 0;
}

// In C programming, \n is a newline escape sequence. It tells the compiler to move the cursor to the next line when printing output.


